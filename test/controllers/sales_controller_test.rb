require 'test_helper'

class SalesControllerTest < ActionController::TestCase
  setup do
    @sale = sales(:one)
  end

  test "should get index" do
    get :index
    assert_response :success
    assert_not_nil assigns(:sales)
  end

  test "should get new" do
    get :new
    assert_response :success
  end

  test "should create sale" do
    assert_difference('Sale.count') do
      post :create, sale: { forma_de_pago: @sale.forma_de_pago, ganancia: @sale.ganancia, precio_bruto: @sale.precio_bruto, precio_neto: @sale.precio_neto, quien_cargo: @sale.quien_cargo }
    end

    assert_redirected_to sale_path(assigns(:sale))
  end

  test "should show sale" do
    get :show, id: @sale
    assert_response :success
  end

  test "should get edit" do
    get :edit, id: @sale
    assert_response :success
  end

  test "should update sale" do
    patch :update, id: @sale, sale: { forma_de_pago: @sale.forma_de_pago, ganancia: @sale.ganancia, precio_bruto: @sale.precio_bruto, precio_neto: @sale.precio_neto, quien_cargo: @sale.quien_cargo }
    assert_redirected_to sale_path(assigns(:sale))
  end

  test "should destroy sale" do
    assert_difference('Sale.count', -1) do
      delete :destroy, id: @sale
    end

    assert_redirected_to sales_path
  end
end
